void m4w_debug(const char *format, ...)
{
	static int first=0;
	static const char *debug_file = M4W_LOGDIR"/m4w_loader.log";
	va_list aptr;
	FILE *fp;

	if(first == 0) {
		fp = fopen(debug_file, "wb");
		if(fp != NULL) {
			fprintf(fp,"%s Initialized...\n",__FUNCTION__);
			fflush(fp);
		}
		first++;
	}
	else {
		fp = fopen(debug_file, "a+b");
	}
	
	if(fp != NULL) {
		va_start(aptr, format);
		vfprintf(fp, format, aptr);
		va_end(aptr);
		fclose(fp);
	}
}

static int compareGameList(const void *a, const void *b)
{
	int r;
	game_entry_t *aa = (game_entry_t *)a;
	game_entry_t *bb = (game_entry_t *)b;

	r = strcmp(aa->desc, bb->desc);
	return r;
}

game_entry_t *searchPattern(const char *pattern, int type)
{
	game_entry_t *r;
	
	switch(type) {
	case M4W_BYDESC:
		for(int i=0; i<sizeof(game_list)/sizeof(*game_list); i++) {
			if(pattern[0] == game_list[i].desc[0]) {
				if(strncasecmp(pattern,game_list[i].desc,strlen(pattern)) == 0)
					return(&game_list[i]);
			}
		}
		break;
	case M4W_BYROM:
		for(int i=0; i<sizeof(game_list)/sizeof(*game_list); i++) {
			if(pattern[0] == game_list[i].rom[0]) {
				if(strcmp(pattern,game_list[i].rom) == 0)
					return(&game_list[i]);
			}
		}
	default:
		r = NULL;
		break;
	}
	return r;
}

int check_game(game_entry_t *p)
{
	char path[PATH_MAX];
	
	if(p == NULL)
		return -1;
	
	snprintf(path,sizeof(path),"%s/%s.zip",M4W_ROMS,p->rom);
	if(access(path, R_OK) != 0) {
		p->flags |= M4W_FLAGS_ROMMISSING;
	}
	snprintf(path,sizeof(path),"%s/%s",M4W_LIBS,p->exe);
	if(access(path, R_OK) != 0) {
		p->flags |= M4W_FLAGS_DOLMISSING;;
	}
	snprintf(path,sizeof(path),"%s/%s",M4W_CRASHDIR,p->rom);
	if(access(path, R_OK) == 0) {
		p->status = CRASHED;
	}
	else {
		p->status = OK;
	}
	return 0;
}

game_entry_t *initGamelist(int *n_games)
{
	const int n = sizeof(game_list)/sizeof(*game_list);
	
	qsort((void *)game_list, n, sizeof(game_entry_t),compareGameList);

	if(n_games != NULL)
		*n_games = n;

	return game_list;
}
